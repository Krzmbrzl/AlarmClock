package alarm;

/**
 * This enum contains the values for repetition types
 * 
 * @author Raven
 *
 */
public enum ERepetition {
	/**
	 * No repetition
	 */
	NONE,
	/**
	 * Repeat every minute
	 */
	MINUTELY,
	/**
	 * Repeat every hour
	 */
	HOURLY,
	/**
	 * Repeat every day
	 */
	DAYLY,
	/**
	 * Repeat every week
	 */
	WEEKLY
}
